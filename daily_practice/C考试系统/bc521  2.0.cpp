/*---------------------------------------------
任意输入两个字符串（使用数组存储，串长度不超过19个字符，允许出现空格字符，并且一个串内字符不重复），
求两串中相同的字符（按相同字符在第一个串的先后顺序输出）。
输入：poiuytr
      asofigjkutmn
输出：oiut
----------------------------------------------*/
#include<stdio.h>
int main()
{
	char a[100],b[100];
	gets(a);
	gets(b);
	for(int i=0;a[i]!='\0';i++)
	{
		for(int j=0;b[j]!='\0';j++)
		{
			if(a[i]==b[j])
			{
				printf("%c",a[i]);
			}
		} 
	}
} 

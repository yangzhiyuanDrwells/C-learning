/*------------------------------------------------------
请编写程序，从键盘上输入10个整数，放在数组中，
要求计算最大数和最小数的差。
说明：10个数用空格分隔。
输入： 8  3  10  34  1  22  7  9  12  23
输出： dif=33
----------------------------------------------------*/
#include <stdio.h>
int main()
{
	int a[10];
	int i,max,min;
	for(i=0;i<10;i++)
	scanf("%d",&a[i]);
	max=a[0],min=a[0];
	for(i=0;i<10;i++)
	{
	if(max<a[i])max=a[i];
	if(min>a[i])min=a[i];
	}
	printf("dif=%d",max-min);
}

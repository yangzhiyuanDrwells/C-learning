/*------------------------------------------------
功能：编写函数，计算给定整数n的所有因子之和（不包括1
      与自身）。
注意：n的值不大于1000。
例如：
输入：855
输出：704

------------------------------------------------*/

#include <stdio.h>

   
int fun(int n)
{
  
  /**********Program**********/
	int sum=0;

	for(int i=2;i<n;i++){
		if(n%i==0) sum+=i;
	}
	return sum;
  /**********  End  **********/
}

void main()
{int n;
	scanf("%d",&n);
  	printf("%d\n",fun(n));
  
}

